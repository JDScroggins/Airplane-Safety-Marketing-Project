library(tidyverse)
donut <- donut %>% arrange(country)
donut$fraction = donut$dogs / sum(donut$dogs)
donut$ymax = cumsum(donut$fraction)
donut$ymin = c(0, head(donut$ymax, n=-1))

ggplot(donut, aes(ymax=ymax, ymin=ymin, xmax=4, xmin=3, fill=country))  + geom_rect(stat = "identity") +
